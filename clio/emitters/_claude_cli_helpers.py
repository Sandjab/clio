def _chain_has_for_each(chain) -> bool:
    """Recursively scan a flow chain for any ForEachIR node."""
    for item in chain:
        if isinstance(item, ForEachIR):
            return True
        # Nested CallIR carries no children; nothing else to recurse into.
    return False


def _flatten_calls(chain) -> list:
    """Recursively yield every CallIR in `chain`, descending into nested
    ForEachIR.body."""
    out: list = []
    for item in chain:
        if isinstance(item, CallIR):
            out.append(item)
        elif isinstance(item, ForEachIR):
            out.extend(_flatten_calls(item.body))
    return out


def _is_primitive_type(t) -> bool:
    """True if `t` is one of int|float|str|bool. Used to decide between
    `jq -r` (primitive → unwrapped) and `jq -c` (object → JSON literal)."""
    if isinstance(t, ConstrainedType):
        return _is_primitive_type(t.base)
    return isinstance(t, PrimitiveType)


def _resolve_iter_inner_type(t):
    """Return the element type of a ListType, or None for any other shape."""
    return t.inner if isinstance(t, ListType) else None


def _render_prompt(step: StepIR) -> str:
    input_block = "\n".join(f"  {f.name}: ${{{f.name}}}" for f in step.takes) or "  (no inputs)"
    return _JUDGMENT_PROMPT_TEMPLATE.format(name=step.name, input_block=input_block)


def _field_doc(f: FieldIR) -> str:
    return f"TAKES: {f.name}: {_render_type(f.type)}"


def _render_type(t) -> str:
    if isinstance(t, PrimitiveType):
        return t.name
    return type(t).__name__


def _server_spec_dict_repr_cli(spec: McpServerSpecIR) -> str:
    """Render an `McpServerSpecIR` as a Python dict literal usable in
    emitted claude-cli step scripts. Mirrors the python-target helper."""
    if isinstance(spec, StdioServerSpecIR):
        return (
            "{"
            f"'name': {spec.name!r}, "
            f"'transport': 'stdio', "
            f"'command': {spec.command!r}, "
            f"'args': {list(spec.args)!r}, "
            f"'env': {list(spec.env)!r}"
            "}"
        )
    if isinstance(spec, SseServerSpecIR):
        return (
            "{"
            f"'name': {spec.name!r}, "
            f"'transport': 'sse', "
            f"'url': {spec.url!r}, "
            f"'headers': {list(spec.headers)!r}"
            "}"
        )
    if isinstance(spec, HttpServerSpecIR):
        return (
            "{"
            f"'name': {spec.name!r}, "
            f"'transport': 'http', "
            f"'url': {spec.url!r}, "
            f"'headers': {list(spec.headers)!r}"
            "}"
        )
    raise ValueError(f"unknown McpServerSpecIR subtype: {type(spec).__name__}")


def emit_mcp_tool_step_cli(
    step: StepIR,
    impl: McpToolImplIR,
    spec: McpServerSpecIR,
    io_doc: str,
) -> str:
    """Emit a standalone Python script for a claude-cli mcp_tool step.

    Each step is run by `bash run.sh` as `python steps/NN_name.py --takes...`.
    The script boots an MCP client (per-step — claude-cli has no place to
    hold a long-lived client across subprocess invocations), calls the
    requested tool, writes the result into `state.json`, then exits.
    """
    out_name = step.gives.name if step.gives else "result"
    argparse_lines = [
        f'    parser.add_argument("--{f.name}", required=True)' for f in step.takes
    ] or ["    pass"]

    takes_dict_lines: list[str] = ["    _takes = {"]
    for f in step.takes:
        takes_dict_lines.append(f"        {f.name!r}: args.{f.name},")
    takes_dict_lines.append("    }")
    if not step.takes:
        takes_dict_lines = ["    _takes = {}"]

    server_repr = _server_spec_dict_repr_cli(spec)
    args_repr = "{" + ", ".join(f"{k!r}: {v!r}" for k, v in impl.args) + "}"

    body = (
        f'"""Step: {step.name} (exact, impl: mcp_tool)\n\n'
        f'{io_doc}\n\n'
        "Auto-generated by clio from `impl: mode: mcp_tool`. The bundled\n"
        "clio_runtime.mcp_client module provides the client + transport\n"
        "handling. Each step boots a per-call client (claude-cli has no\n"
        "long-lived orchestrator).\n"
        '"""\n'
        "import argparse\n"
        "import asyncio\n"
        "import json\n"
        "import sys\n"
        "from pathlib import Path\n\n"
        "# The compiled output bundles `clio_runtime/mcp_client.py` next to the\n"
        "# steps/ directory. Add the output root to sys.path so the import works\n"
        "# whatever the cwd of the bash orchestrator.\n"
        "sys.path.insert(0, str(Path(__file__).resolve().parent.parent))\n"
        "from clio_runtime import mcp_client as _mcp  # noqa: E402\n\n\n"
        "STATE_FILE = Path(__file__).resolve().parent.parent / 'state.json'\n\n\n"
        "def main() -> int:\n"
        "    parser = argparse.ArgumentParser()\n"
        + "\n".join(argparse_lines) + "\n"
        "    args = parser.parse_args()\n"
        + "\n".join(takes_dict_lines) + "\n"
        f"    _server = {server_repr}\n"
        f"    _args = {args_repr}\n"
        f"    {out_name} = asyncio.run(_mcp.call_tool_async(\n"
        f"        _server, {impl.tool!r}, _args, _takes,\n"
        f"        timeout={impl.timeout_seconds}, parse={impl.parse!r},\n"
        f"    ))\n\n"
        "    state = json.loads(STATE_FILE.read_text()) if STATE_FILE.exists() else {}\n"
        f'    state["{out_name}"] = {out_name}\n'
        "    STATE_FILE.write_text(json.dumps(state, indent=2))\n"
        "    return 0\n\n\n"
        "if __name__ == '__main__':\n"
        "    raise SystemExit(main())\n"
    )
    return body
