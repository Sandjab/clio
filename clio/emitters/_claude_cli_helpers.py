def emit_rest_step_cli(step, impl, io_doc: str) -> str:
    """Emit a standalone Python script for a claude-cli REST step.

    Each step is run by `bash run.sh` as `python steps/NN_name.py --takes...`
    and reads/writes `state.json` at the repo root. The script imports the
    bundled `clio_runtime.rest` module (templating + retry helpers) by
    adding the parent dir to `sys.path` at startup.
    """
    out_name = step.gives.name if step.gives else "result"
    argparse_lines = [
        f'    parser.add_argument("--{f.name}", required=True)' for f in step.takes
    ] or ["    pass"]

    takes_dict_lines: list[str] = ["    _takes = {"]
    for f in step.takes:
        takes_dict_lines.append(f"        {f.name!r}: args.{f.name},")
    takes_dict_lines.append("    }")
    if not step.takes:
        takes_dict_lines = ["    _takes = {}"]

    kwargs_lines: list[str] = ["    _kwargs: dict = {}"]
    if impl.query is not None:
        kwargs_lines.append(
            f"    _kwargs['params'] = _rest.render_dict({tuple(impl.query)!r}, _takes)"
        )
    headers_seeded = False
    if impl.headers is not None:
        kwargs_lines.append(
            f"    _kwargs['headers'] = _rest.render_dict({tuple(impl.headers)!r}, _takes)"
        )
        headers_seeded = True

    if impl.body is not None:
        body_kind = type(impl.body).__name__
        if body_kind == "JsonBodyIR":
            kwargs_lines.append(
                f"    _kwargs['json'] = _rest.render_dict({tuple(impl.body.fields)!r}, _takes)"
            )
        elif body_kind == "RawBodyIR":
            kwargs_lines.append(
                f"    _kwargs['data'] = _rest.subst({impl.body.template!r}, _takes)"
            )
            if not headers_seeded:
                kwargs_lines.append("    _kwargs.setdefault('headers', {})")
                headers_seeded = True
            kwargs_lines.append(
                "    _kwargs['headers'].setdefault('Content-Type', 'text/plain')"
            )
        elif body_kind == "FileBodyIR":
            kwargs_lines.append(
                f"    _data, _ct = _rest.read_file_body({impl.body.path!r}, _takes)"
            )
            kwargs_lines.append("    _kwargs['data'] = _data")
            if not headers_seeded:
                kwargs_lines.append("    _kwargs.setdefault('headers', {})")
                headers_seeded = True
            kwargs_lines.append("    _kwargs['headers'].setdefault('Content-Type', _ct)")
        elif body_kind == "FormBodyIR":
            kwargs_lines.append(
                f"    _kwargs['data'] = _rest.render_dict({tuple(impl.body.fields)!r}, _takes)"
            )
        elif body_kind == "MultipartBodyIR":
            kwargs_lines += [
                "    _form: dict = {}",
                "    _files: dict = {}",
                f"    for _k, _v in {tuple(impl.body.fields)!r}:",
                "        if isinstance(_v, str) and _v.startswith('@'):",
                "            _path = _v[1:]",
                "            with open(_path, 'rb') as _f:",
                (
                    "                _files[_k] = (Path(_path).name, "
                    "_f.read(), _rest.content_type_for_path(_path))"
                ),
                "        else:",
                (
                    "            _form[_k] = _rest.subst(_v, _takes) "
                    "if isinstance(_v, str) else _v"
                ),
                "    if _form:",
                "        _kwargs['data'] = _form",
                "    if _files:",
                "        _kwargs['files'] = _files",
            ]

    if impl.timeout_seconds is not None:
        kwargs_lines.append(f"    _kwargs['timeout'] = {impl.timeout_seconds}")
    else:
        kwargs_lines.append("    _kwargs['timeout'] = None")

    if impl.retry is not None:
        request_lines = [
            f"    _attempts = {impl.retry.attempts}",
            f"    _retry_on = {tuple(impl.retry.on)!r}",
            f"    _backoff = {impl.retry.backoff!r}",
            f"    _base = {impl.retry.base}",
            f"    _cap = {impl.retry.cap}",
            "    response = None",
            "    for _i in range(_attempts):",
            "        try:",
            f"            response = requests.request(method={impl.method!r}, url=_url, **_kwargs)",
            "        except Exception as _e:",
            "            if _rest.is_retryable_exception(_e, _retry_on) and _i + 1 < _attempts:",
            "                import time as _time",
            "                _time.sleep(_rest.compute_delay(_i + 1, _base, _cap, _backoff))",
            "                continue",
            "            raise",
            "        if (_rest.is_retryable_response(response.status_code, _retry_on)",
            "                and _i + 1 < _attempts):",
            "            _ra = _rest.parse_retry_after(response.headers.get('Retry-After'))",
            "            import time as _time",
            "            _time.sleep(_ra if _ra is not None else _rest.compute_delay(_i + 1, _base, _cap, _backoff))",
            "            continue",
            "        break",
            "    assert response is not None",
        ]
    else:
        request_lines = [
            f"    response = requests.request(method={impl.method!r}, url=_url, **_kwargs)",
        ]

    response_path_repr = repr(impl.response_path)

    body = (
        f'"""Step: {step.name} (exact, impl: rest)\n\n'
        f'{io_doc}\n\n'
        "Auto-generated by clio from `impl: mode: rest`. URL, query, headers,\n"
        "and body string values support ${var} substitution from TAKES and\n"
        "full-value env:NAME resolution. The bundled clio_runtime.rest module\n"
        "(at the output root) provides the templating + retry helpers.\n"
        '"""\n'
        "import argparse\n"
        "import json\n"
        "import re as _re\n"
        "import sys\n"
        "from pathlib import Path\n\n"
        "import requests\n\n"
        "# The compiled output bundles `clio_runtime/rest.py` next to the steps/\n"
        "# directory. Add the output root to sys.path so the import works whatever\n"
        "# the cwd of the bash orchestrator.\n"
        "sys.path.insert(0, str(Path(__file__).resolve().parent.parent))\n"
        "from clio_runtime import rest as _rest  # noqa: E402\n\n\n"
        "STATE_FILE = Path(__file__).resolve().parent.parent / 'state.json'\n\n\n"
        "def _traverse(data, path):\n"
        "    if not path:\n"
        "        return data\n"
        "    for part in _re.findall(r'[^.\\[\\]]+|\\[\\d+\\]', path):\n"
        "        if part.startswith('['):\n"
        "            data = data[int(part[1:-1])]\n"
        "        else:\n"
        "            data = data[part]\n"
        "    return data\n\n\n"
        "def main() -> int:\n"
        "    parser = argparse.ArgumentParser()\n"
        + "\n".join(argparse_lines) + "\n"
        "    args = parser.parse_args()\n"
        + "\n".join(takes_dict_lines) + "\n"
        f"    _url = _rest.subst({impl.url!r}, _takes)\n"
        + "\n".join(kwargs_lines) + "\n"
        + "\n".join(request_lines) + "\n"
        "    response.raise_for_status()\n"
        f"    {out_name} = _traverse(response.json(), {response_path_repr})\n\n"
        "    state = json.loads(STATE_FILE.read_text()) if STATE_FILE.exists() else {}\n"
        f'    state["{out_name}"] = {out_name}\n'
        "    STATE_FILE.write_text(json.dumps(state, indent=2))\n"
        "    return 0\n\n\n"
        "if __name__ == '__main__':\n"
        "    raise SystemExit(main())\n"
    )
    return body


